    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here
        String insert = this.input.getText();

        if (insert == "") {
            return;
        }

        Integer inserido = Integer.parseInt(insert);

        numList.add(inserido);
        if (inserido % 2 == 0) {
            par.add(inserido);
        } else {
            impar.add(inserido);
        }
        nums++;

        pares.setText(par.toString());
        impares.setText(impar.toString());
        listaCompleta.setText(numList.toString());

        input.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

